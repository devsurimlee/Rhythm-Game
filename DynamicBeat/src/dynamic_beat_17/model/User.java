package dynamic_beat_17.model;

public class User {
	
	private String userid;
	private char[] passwd;
	
	
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public char[] getPasswd() {
		return passwd;
	}
	public void setPasswd(char[] cs) {
		this.passwd = cs;
	}
	

}
